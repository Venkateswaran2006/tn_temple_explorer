function toggleLang() {
  // Cycle: en -> ta -> hi -> te -> kn -> ml -> bn -> mr -> en
  const langs = ["en", "ta", "hi", "te", "kn", "ml", "bn", "mr"];
  let currentIndex = langs.indexOf(lang);
  currentIndex = (currentIndex + 1) % langs.length;
  lang = langs[currentIndex];

  updateUI();
  if (currentPlace) {
    loadPlace(currentPlace);
  }
}

function updateUI() {
  const t = content[lang];

  // Static UI
  const mainTitleEntry = document.getElementById("mainTitle");
  if (mainTitleEntry) mainTitleEntry.innerText = t.title;

  const btnLangEntry = document.getElementById("btnLang");
  if (btnLangEntry) btnLangEntry.innerText = t.langBtn;

  const btnDarkEntry = document.getElementById("btnDark");
  if (btnDarkEntry) btnDarkEntry.innerText = t.darkBtn;

  const heroTitleEntry = document.getElementById("heroTitle");
  if (heroTitleEntry) heroTitleEntry.innerText = t.heroTitle;

  const heroSubEntry = document.getElementById("heroSub");
  if (heroSubEntry) heroSubEntry.innerText = t.heroSub;

  const placesTitleEntry = document.getElementById("placesTitle");
  if (placesTitleEntry) placesTitleEntry.innerText = t.placesTitle;

  const feedbackTitle = document.querySelector(".feedback h2");
  if (feedbackTitle) feedbackTitle.innerText = t.feedbackTitle;

  const fname = document.getElementById("fname");
  if (fname) fname.placeholder = t.namePlaceholder;

  const fmsg = document.getElementById("fmsg");
  if (fmsg) fmsg.placeholder = t.msgPlaceholder;

  const btnSubmit = document.getElementById("btnSubmit");
  if (btnSubmit) btnSubmit.innerText = t.submitBtn;

  const footerText = document.getElementById("footerText");
  if (footerText) footerText.innerText = t.footer;

  const searchBar = document.getElementById("searchBar");
  if (searchBar) searchBar.placeholder = t.searchPlaceholder;

  // Update Cards in list
  for (const key in placesData) {
    const cardTitle = document.querySelector(`.card[onclick="loadPlace('${key}')"] h3`);
    const cardDesc = document.querySelector(`.card[onclick="loadPlace('${key}')"] p`);
    if (cardTitle && cardDesc) {
      cardTitle.innerText = placesData[key][lang].name;
      cardDesc.innerText = placesData[key][lang].desc;
    }
  }
}

function loadPlace(place) {
  currentPlace = place;
  const d = document.getElementById("details");
  const p = placesData[place];
  const t = p[lang]; // Get English, Tamil or Hindi data

  d.classList.remove("hidden");

  const templesList = t.temples.map(item => `<li>${item}</li>`).join("");

  d.innerHTML = `
    <h2>${t.name}</h2>
    <img src="${p.detailsImg}" onerror="this.onerror=null; this.src='https://via.placeholder.com/800x400?text=Image+Not+Found'">
    <p>${t.details}</p>

    <h3>${t.templeTitle}</h3>
    <ul>${templesList}</ul>

    <h3>${t.locTitle}</h3>
    <iframe src="${p.map}"></iframe>
  `;

  d.scrollIntoView({ behavior: "smooth" });
}

